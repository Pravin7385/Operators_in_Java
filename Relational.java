package com.operators;

// Relational Operator

public class Relational {

	public static void main(String[] args) {
		int a = 30;
		int b = 40;
		
		System.out.println("Greater then is : " + (a>b));
		System.out.println("Less then is : " + (a<b));
		System.out.println("Greater then Equalto is : " + (a>=b));
		System.out.println("Less then equal is : " + (a<=b));
		System.out.println("equals to equals is : " + (a==b));
		System.out.println("equals to equals is : " + (a!=b) + "\n");
		
//Unary Operators
		int x = 20;
		System.out.println("1.Increment is " + x++);
		System.out.println("2.Decrement is " + x--);

	}
}
